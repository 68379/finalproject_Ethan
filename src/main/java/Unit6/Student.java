package Unit6;

public class Student {
    private String name;
    private int age;
    private double grade;

    //10/10
    public Student(String name, int age, double grade) {
        this.name = name;
        this.age = age;
        this.grade = grade;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public double getGrade() {
        return grade;
    }

    public void setGrade(double grade) {
        this.grade = grade;
    }

    @Override
    public String toString(){
        return "Name: " + getName() + ", Age: " + getAge() + ", Grade Average: " + getGrade();
    }
}
